/**
 * Export per-market ASO upload brief (captions + screenshot spec).
 * Usage: pnpm export:aso-brief
 */
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { readFileSync } from "node:fs";
import { STORE_MARKETS, STORE_PRIVACY_URL, STORE_SUPPORT_URL } from "@huhu/shared";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const listingsDir = join(root, "apps/api/src/data/store-listings");
const outDir = join(root, "dist/aso-brief");

mkdirSync(outDir, { recursive: true });

const spec = `Screenshot spec (see docs/ASO_SCREENSHOTS.md):
- iPhone 6.7" (ASC): 1284×2778
- Google Play phone: 1080×1920 minimum
`;

for (const { market, listingFile } of STORE_MARKETS) {
  const listing = JSON.parse(
    readFileSync(join(listingsDir, `${listingFile}.json`), "utf8"),
  );
  const lines = [
    `# ASO brief — ${market} (${listing.locale})`,
    "",
    "## App Store",
    `- Name: ${listing.appStore.name}`,
    `- Subtitle: ${listing.appStore.subtitle}`,
    `- Keywords: ${listing.appStore.keywords}`,
    "",
    "## Google Play",
    `- Title: ${listing.googlePlay.title}`,
    `- Short: ${listing.googlePlay.shortDescription}`,
    "",
    "## Store URLs (App Information / Privacy)",
    `- Support: ${STORE_SUPPORT_URL}`,
    `- Privacy policy: ${STORE_PRIVACY_URL}`,
    "",
    "## Screenshots (order)",
    ...(listing.screenshotCaptions ?? []).map(
      (c, i) => `${i + 1}. ${c}`,
    ),
    "",
  ];
  const shotDir = join(root, "dist/aso-screenshots", market);
  const shotFiles = [
    "01-chat.png",
    "02-modes.png",
    "03-character-creator.png",
    "04-diary.png",
    "05-subscribe.png",
    "06-privacy.png",
  ];
  const captured = shotFiles.filter((f) => existsSync(join(shotDir, f)));
  if (captured.length > 0) {
    lines.push("## Captured PNG (local)");
    for (const f of captured) {
      lines.push(`- dist/aso-screenshots/${market}/${f}`);
    }
    lines.push("");
  }
  const capDir = join(root, "dist/aso-screenshots-captioned", market);
  const capPlay = join(root, "dist/aso-screenshots-play-captioned", market);
  const capFiles = shotFiles.filter((f) => existsSync(join(capDir, f)));
  if (capFiles.length > 0) {
    lines.push("## Captioned PNG (recommended for stores)");
    for (const f of capFiles) {
      lines.push(`- dist/aso-screenshots-captioned/${market}/${f}`);
      if (existsSync(join(capPlay, f))) {
        lines.push(`- dist/aso-screenshots-play-captioned/${market}/${f}`);
      }
    }
    lines.push("");
  }
  lines.push(spec);
  const path = join(outDir, `${market}.md`);
  writeFileSync(path, lines.join("\n"));
  console.log(`wrote ${path}`);
}

writeFileSync(
  join(outDir, "README.md"),
  [
    "# ASO upload briefs",
    "",
    "Generated by `pnpm export:aso-brief`. Copy captions into store consoles.",
    "Pair with screenshots from docs/ASO_SCREENSHOTS.md.",
    "",
    ...STORE_MARKETS.map((m) => `- [${m.market}.md](./${m.market}.md)`),
  ].join("\n"),
);
console.log(`wrote ${join(outDir, "README.md")}`);
