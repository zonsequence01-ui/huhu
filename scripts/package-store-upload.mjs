/**
 * Assemble per-market store upload folders (App Store + Google Play PNGs, brief, catalog).
 * Usage: pnpm package:store-upload
 */
import { cpSync, existsSync, mkdirSync, rmSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";
import { STORE_MARKETS } from "@huhu/shared";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const outRoot = join(root, "dist/store-upload");

function run(cmd, args) {
  const r = spawnSync(cmd, args, { cwd: root, shell: true, stdio: "inherit" });
  if (r.status !== 0) process.exit(r.status ?? 1);
}

const catalogDir = join(root, "dist/store-catalog");
if (!existsSync(catalogDir)) {
  console.log("Exporting store catalog…");
  run("pnpm", ["export:store-catalog:files"]);
}

const iapChecklistDir = join(root, "dist/iap-store-checklist");
if (!existsSync(join(iapChecklistDir, "README.md"))) {
  console.log("Exporting IAP store checklist…");
  run("pnpm", ["export:iap-store-checklist"]);
}

if (!existsSync(join(root, "dist/aso-brief/tw.md"))) {
  console.log("Exporting ASO brief…");
  run("pnpm", ["export:aso-brief"]);
}

const appStoreRoot = join(root, "dist/asc-iphone69");
const playRoot = join(root, "dist/aso-screenshots-play");

if (!existsSync(join(root, "dist/aso-screenshots/tw/01-chat.png"))) {
  console.error("Missing App Store PNGs. Run: pnpm capture:aso-screenshots -- --all");
  process.exit(1);
}

if (!existsSync(join(appStoreRoot, "tw", "01-chat.png"))) {
  console.log("Resizing App Store screenshots to 1284×2778…");
  run("pnpm", ["resize:aso-app-store-screenshots", "--", "--all"]);
}

if (!existsSync(join(playRoot, "tw", "01-chat.png"))) {
  console.log("Resizing Google Play screenshots…");
  run("pnpm", ["resize:aso-play-screenshots", "--", "--all"]);
}

const appCaptionedRoot = join(root, "dist/aso-screenshots-captioned");
const playCaptionedRoot = join(root, "dist/aso-screenshots-play-captioned");
const captionedComplete = STORE_MARKETS.every(
  ({ market }) =>
    existsSync(join(appCaptionedRoot, market, "01-chat.png")) &&
    existsSync(join(playCaptionedRoot, market, "01-chat.png")),
);
if (!captionedComplete) {
  console.log("Composing screenshot captions…");
  run("pnpm", ["compose:aso-screenshots", "--", "--all"]);
}

rmSync(outRoot, { recursive: true, force: true });
mkdirSync(outRoot, { recursive: true });

for (const { market } of STORE_MARKETS) {
  const dest = join(outRoot, market);
  mkdirSync(dest, { recursive: true });

  cpSync(join(appStoreRoot, market), join(dest, "app-store"), { recursive: true });
  cpSync(join(playRoot, market), join(dest, "google-play"), { recursive: true });
  if (existsSync(join(appCaptionedRoot, market))) {
    cpSync(
      join(appCaptionedRoot, market),
      join(dest, "app-store-captioned"),
      { recursive: true },
    );
  }
  if (existsSync(join(playCaptionedRoot, market))) {
    cpSync(
      join(playCaptionedRoot, market),
      join(dest, "google-play-captioned"),
      { recursive: true },
    );
  }

  const brief = join(root, "dist/aso-brief", `${market}.md`);
  if (existsSync(brief)) {
    cpSync(brief, join(dest, `${market}-aso-brief.md`));
  }

  const catalog = join(catalogDir, `${market}.json`);
  if (existsSync(catalog)) {
    cpSync(catalog, join(dest, `${market}-store-catalog.json`));
  }

  const iapMd = join(iapChecklistDir, `${market}.md`);
  if (existsSync(iapMd)) {
    cpSync(iapMd, join(dest, `${market}-iap-checklist.md`));
  }

  console.log(`✓ dist/store-upload/${market}/`);
}

if (existsSync(join(iapChecklistDir, "README.md"))) {
  cpSync(join(iapChecklistDir, "README.md"), join(outRoot, "iap-checklist-README.md"));
}

const graphicsDir = join(root, "dist/store-graphics");
if (existsSync(join(graphicsDir, "icon-512.png"))) {
  mkdirSync(join(outRoot, "tw"), { recursive: true });
  cpSync(join(graphicsDir, "icon-512.png"), join(outRoot, "tw", "icon-512.png"));
  cpSync(
    join(graphicsDir, "feature-graphic-1024x500.png"),
    join(outRoot, "tw", "feature-graphic-1024x500.png"),
  );
  console.log("✓ dist/store-upload/tw/ (icon + feature graphic)");
}

writeFileSync(
  join(outRoot, "README.md"),
  [
    "# Store upload bundle",
    "",
    "Generated by `pnpm package:store-upload`.",
    "",
    "Per market folder:",
    "- `app-store/` / `app-store-captioned/` — 1284×2778 (ASC iPhone 6.7\"; captioned recommended)",
    "- `google-play/` / `google-play-captioned/` — 1080×1920",
    "- `{market}-aso-brief.md` — captions + metadata",
    "- `{market}-store-catalog.json` — PPP + SKU reference",
    "- `{market}-iap-checklist.md` — App Store / Play SKU setup",
    "- `tw/icon-512.png`, `tw/feature-graphic-1024x500.png` — Play graphics (after `pnpm generate:store-graphics`)",
    "- `iap-checklist-README.md` — all markets summary",
    "",
    ...STORE_MARKETS.map((m) => `- [${m.market}/](./${m.market}/)`),
  ].join("\n"),
);

console.log("\nStore upload bundle ready at dist/store-upload/");

const validate = spawnSync("pnpm", ["validate:store-upload"], {
  cwd: root,
  shell: true,
  stdio: "inherit",
});
if (validate.status !== 0) process.exit(validate.status ?? 1);
