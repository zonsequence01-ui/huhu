/**
 * Generate Google Play icon (512×512) and feature graphic (1024×500).
 * Usage: pnpm generate:store-graphics
 */
import { mkdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { chromium } from "@playwright/test";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const outDir = join(root, "dist/store-graphics");

const brand = {
  bg: "linear-gradient(145deg, #1a1033 0%, #2d1b69 45%, #5b4cdb 100%)",
  accent: "#ffb4d9",
  text: "#ffffff",
};

function iconHtml() {
  return `<!DOCTYPE html><html><head><meta charset="utf-8"><style>
    *{margin:0;padding:0;box-sizing:border-box}
    body{width:512px;height:512px;background:${brand.bg};display:flex;align-items:center;justify-content:center;font-family:system-ui,sans-serif}
    .wrap{text-align:center;color:${brand.text}}
    .mark{font-size:120px;line-height:1;font-weight:800;letter-spacing:-4px;text-shadow:0 8px 32px rgba(0,0,0,.35)}
    .sub{margin-top:8px;font-size:28px;font-weight:600;color:${brand.accent}}
  </style></head><body><div class="wrap"><div class="mark">呼</div><div class="sub">Huhu</div></div></body></html>`;
}

function featureHtml() {
  return `<!DOCTYPE html><html><head><meta charset="utf-8"><style>
    *{margin:0;padding:0;box-sizing:border-box}
    body{width:1024px;height:500px;background:${brand.bg};display:flex;align-items:center;padding:0 64px;font-family:system-ui,sans-serif;color:${brand.text}}
    .logo{font-size:96px;font-weight:800;margin-right:40px;text-shadow:0 6px 24px rgba(0,0,0,.35)}
    .copy h1{font-size:52px;font-weight:800;margin-bottom:12px}
    .copy p{font-size:28px;color:${brand.accent};max-width:640px;line-height:1.35}
  </style></head><body>
    <div class="logo">呼</div>
    <div class="copy">
      <h1>呼呼 Huhu</h1>
      <p>記得住你的 AI 伴侶 · 體力自然恢復 · RAG 長期記憶</p>
    </div>
  </body></html>`;
}

async function capture(html, path, width, height) {
  const browser = await chromium.launch();
  const page = await browser.newPage({ viewport: { width, height } });
  await page.setContent(html, { waitUntil: "networkidle" });
  await page.screenshot({ path, type: "png" });
  await browser.close();
}

mkdirSync(outDir, { recursive: true });

const iconPath = join(outDir, "icon-512.png");
const featurePath = join(outDir, "feature-graphic-1024x500.png");

await capture(iconHtml(), iconPath, 512, 512);
await capture(featureHtml(), featurePath, 1024, 500);

writeFileSync(
  join(outDir, "README.md"),
  [
    "# Store graphics",
    "",
    "- `icon-512.png` — Google Play / marketing icon (512×512)",
    "- `feature-graphic-1024x500.png` — Google Play feature graphic",
    "",
    "Generated by `pnpm generate:store-graphics`.",
    "",
  ].join("\n"),
);

console.log(`✓ ${iconPath}`);
console.log(`✓ ${featurePath}`);
